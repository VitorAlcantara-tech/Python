# FUNCAO CALCULADORA
n1 = int(input("Digite o primeiro número: "))
n2 = int(input("Digite o segundo número: "))
operacao = input("Digite a operação da conta: ")

while operacao not in ['+', '-', '*', '/']: #not in == se operacao nao for nenhum item dentro da lista
    print("Operação inválida. Tente novamente.")
    operacao = input("Digite a operação da conta (+, -, *, /): ")

if operacao == '+':
    soma = n1 + n2
    print(f"A soma dos números {n1} e {n2} é igual {soma}.")

elif operacao == '-':
    subtracao = n1 - n2
    print(f"A subtração dos números {n1} e {n2} é igual {subtracao}.")

elif operacao == '*':
    multiplicacao = n1 * n2
    print(f"A multiplicação dos números {n1} e {n2} é igual {multiplicacao}.")

elif operacao == '/':
    if n2 == 0:
        print("Erro: divisão por zero não é permitida.")
    else:
        divisao = n1 / n2
        print(f"A divisão dos números {n1} e {n2} é igual {divisao}.")


